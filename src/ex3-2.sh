#!/bin/bash

# 사용자로부터 2개 이상의 숫자 입력 받기
echo "값을 2개 이상 입력하세요 (띄어쓰기로 구분)"
read -a arr  # 공백으로 구분된 숫자들을 배열로 입력받음

# 입력된 값 확인
echo "입력된 값: ${arr[@]}"

# 배열의 각 숫자에 대해 y = 0.5 * x^2 계산
for x in "${arr[@]}"; do
  # 계산식: 0.5 * x^2 (bc 명령어로 실수 계산)
  y=$(echo "0.5 * $x * $x" | bc -l)  # bc -l 옵션으로 실수 계산을 정확히 처리
  # 계산 결과 출력
  echo "x=$x → y=0.5*x^2 = $y"
done
