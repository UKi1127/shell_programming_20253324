#!/bin/bash

# 두 숫자 입력 받기
echo "두 숫자를 입력하세요: "
read a b  # 사용자로부터 두 숫자 입력 받기

# 입력된 두 숫자 출력
echo "두 숫자: $a, $b"

# 덧셈 연산
echo "덧셈: $((a + b))"

# 뺄셈 연산
echo "뺄셈: $((a - b))"

# 곱셈 연산
echo "곱셈: $((a * b))"

# 나눗셈 연산 (0으로 나누는 경우 처리)
if [ $b -ne 0 ]; then
  # b가 0이 아닐 경우 나눗셈 계산
  echo "나눗셈: $((a / b))"
else
  # b가 0일 경우 나눗셈 불가 메시지 출력
  echo "나눗셈 불가 (0으로 나눔)"
fi
